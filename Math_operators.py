# Ask the user for two numbers
a = int(input("Enter first number: "))
b = int(input("Enter second number: "))

# Arithmetic operators
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Exponent:", a ** b)

# Comparison operators
print("Equal:", a == b)
print("Not equal:", a != b)
print("Greater:", a > b)
print("Less:", a < b)
